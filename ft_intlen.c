#include "libft.h"

int		ft_intlen(long int n)
{
	int	i;

	i = 0;
	if (n < 0)
		n = n * -1;
	if (n == 0)
		return (1);
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
